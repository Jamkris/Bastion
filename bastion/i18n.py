def normalize(lang: str | None) -> str:
    return lang if lang in SUPPORTED else DEFAULT


def translator(lang: str):
    lang = normalize(lang)
    table = TRANSLATIONS[lang]

    def t(key: str) -> str:
        return table.get(key, TRANSLATIONS[DEFAULT].get(key, key))

    return t
